/**
 * Переводит строку по ключу из словаря с поддержкой интерполяции
 * переменных вида {name}.
 *
 * @param key - Ключ перевода (английская строка)
 * @param vars - Опциональный объект с переменными для подстановки в строку
 * @returns Переведённая строка с подставленными значениями
 */
export function t(key: string, vars?: Record<string, string | number>): string {
  let result = dict[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      result = result.replace(`{${k}}`, String(v))
    }
  }
  return result
}

