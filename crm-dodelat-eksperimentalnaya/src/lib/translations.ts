/**
 * Переводит значение enum в человекочитаемую строку.
 *
 * @param type - Имя enum-типа (например, "ClientStatus", "TaskPriority")
 * @param value - Значение enum (например, "ACTIVE", "HIGH")
 * @returns Перевод (например, "Активен", "Высокий") или исходное значение с заменой _ на пробелы
 */
export function translateEnum(type: string, value: string): string {
  return enums[type]?.[value] ?? value.replace(/_/g, " ")
}

